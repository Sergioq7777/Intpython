def suma(n1, n2):
    """ Suma """
    return n1 + n2
def resta(n1, n2):
    """ resta """
    return n1 - n2
def mul(n1, n2):
    """ Multiplicacion """
    return n1 * n2
def div(n1, n2):
    """ Divicion """
    return n1 / n2